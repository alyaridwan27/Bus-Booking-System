    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        // Event handler for mouse click event on jTable1
        // Obtain the DefaultTableModel object from jTable1
        DefaultTableModel d1 = (DefaultTableModel)jTable1.getModel();
        // Get the index of the selected row
        int selected = jTable1.getSelectedRow();
        // Retrieve the value of the "status" column from the selected row
        String status = d1.getValueAt(selected, 2).toString();
        
        // Check if the seat status is not "Booked"
        if(!status.equals("Booked")){
            // Retrieve the seat number and date from the selected row
            String seat = d1.getValueAt(selected, 1).toString();
            String date = d1.getValueAt(selected, 5).toString();
            // Set the seat number and date in the appropriate text fields
            txtSeatNo.setText(seat);
            txtDate.setText(date);
        }
        else{
            // Display a message dialog indicating that the ticket is already booked
            JOptionPane.showMessageDialog(this, "Ticket Booked");
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            DefaultTableModel d1 = (DefaultTableModel)jTable1.getModel();
            int selected = jTable1.getSelectedRow();
            String busno = d1.getValueAt(selected, 0).toString();
            
            // Retrieve the input values from the text fields
            String custName = txtCustName.getText();
            String seat = txtSeatNo.getText();
            String mobile = txtMobNo.getText();
            String date = txtDate.getText();
            
            // Insert a new record into the "busbook" table with the provided values
            pst = con.prepareStatement("insert into busbook(busno, seat, customer, mobile, date)values(?,?,?,?,?)");
            pst.setString(1,busno);
            pst.setString(2,seat);
            pst.setString(3,custName);
            pst.setString(4,mobile);
            pst.setString(5,date);
            pst.executeUpdate();
            
             // Update the "status" column in the "seat" table to "Booked" for the selected seat
            pst1 = con.prepareStatement("update seat set status = ? where seats = ?");
            pst1.setString(1, "Booked");
            pst1.setString(2, seat);
            pst1.executeUpdate();
            
            // Display a message dialog indicating successful booking
            JOptionPane.showMessageDialog(this, "Bus Booked");
            // Reload the data in the jTable1
            Load();
            
            // Clear the input fields
            txtCustName.setText("");
            txtSeatNo.setText("");
            txtMobNo.setText("");
            txtDate.setText("");
            
        } catch (SQLException ex) {
            Logger.getLogger(booking.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

