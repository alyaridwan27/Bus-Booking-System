    //retrieves data from the database and populates a JTable with the results
    public void Load(){
        try {
            // TODO add your handling code here:
            SimpleDateFormat Df = new SimpleDateFormat("yyyy-MM-dd");
            // Format the date obtained from txtDChooser.getDate() and store it as a string
            String date = Df.format(txtDChooser.getDate());
            
            // Prepare and execute an SQL query using a prepared statement
            pst = con.prepareStatement("SELECT seat.busno, seat.seats, seat.status, seat.date, busbook.customer, busbook.mobile from seat Left JOIN busbook ON seat.busno = busbook.busno AND seat.seats = busbook.seat AND seat.date = busbook.date WHERE seat.date = ?");
            pst.setString(1, date);
            rs = pst.executeQuery();
            
            // Retrieve the metadata of the result set
            ResultSetMetaData rsd = rs.getMetaData();
            int c;
            c = rsd.getColumnCount();
            
            // Create a new DefaultTableModel object by casting the model obtained from jTable1.getModel()
            DefaultTableModel d = (DefaultTableModel)jTable1.getModel();
            // Clear any existing rows in the table model
            d.setRowCount(0);
            // Iterate over each row in the result set
            while(rs.next()){
                // Retrieve the column values for each row and add them to the v2 vector
                Vector v2 = new Vector();
                for(int i=1; i<=c; i++){
                    v2.add(rs.getString("busno"));
                    v2.add(rs.getString("seats"));
                    v2.add(rs.getString("status"));
                    v2.add(rs.getString("customer"));
                    v2.add(rs.getString("mobile"));
                    v2.add(rs.getString("date"));
                }
                // Add the v2 vector as a new row to the table model
                d.addRow(v2);
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(booking.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

